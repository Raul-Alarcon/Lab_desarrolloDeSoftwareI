package Models;

public class ViewModelStok {

    private int idStok;
    private String cantidadStok;
    private String descripcion;

    public int getIdStok() {
        return idStok;
    }

    public void setIdStok(int idStok) {
        this.idStok = idStok;
    }

    public String getCantidadStok() {
        return cantidadStok;
    }

    public void setCantidadStok(String cantidadStok) {
        this.cantidadStok = cantidadStok;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

}
